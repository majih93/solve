var increasingTriplet = function (nums) {
  if (nums.length < 3) return false;
  // 최소 숫자 / 중간 숫자를 업데이트하면서 처리해나가면 된다.
  let answer = false;
  let min = nums[0];
  let mid = Number.MAX_SAFE_INTEGER;

  for (const num of nums) {
    // 최소 수 교체
    if (num < min) {
      min = num;
    }

    if (num < mid && num > min) {
      mid = num;
    }

    if (num > mid) {
      answer = true;
      break;
    }
  }

  return answer;
};

